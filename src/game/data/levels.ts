import type {
  BarrelDefinition,
  CheckpointDefinition,
  GeneratorDefinition,
  LevelDefinition,
  PickupDefinition,
  PropDefinition,
  SpawnDefinition,
} from '../core/types';

function parseGrid(rows: string[]): number[][] {
  return rows.map((row) => row.split('').map((tile) => Number(tile)));
}

function enemy(id: string, archetype: SpawnDefinition['archetype'], x: number, y: number, encounterTag?: string): SpawnDefinition {
  return { id, archetype, x, y, encounterTag };
}

function pickup(id: string, type: PickupDefinition['type'], x: number, y: number): PickupDefinition {
  return { id, type, x, y };
}

function barrel(id: string, x: number, y: number): BarrelDefinition {
  return { id, x, y };
}

function generator(id: string, x: number, y: number, activeOnStart = false): GeneratorDefinition {
  return { id, x, y, health: 120, activeOnStart };
}

function checkpoint(
  id: string,
  label: string,
  zone: CheckpointDefinition['zone'],
  respawn: CheckpointDefinition['respawn'],
  beacon: CheckpointDefinition['beacon'],
): CheckpointDefinition {
  return { id, label, zone, respawn, beacon };
}

function prop(id: string, type: PropDefinition['type'], x: number, y: number): PropDefinition {
  return { id, type, x, y };
}

